#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	const char *s;
	char *d;
	int i;

	s = src;
	d = dst;
	i = 0;
	while (i < n)
	{
		d[i] = s[i];
		i++;
	}
	return (dst);
}
